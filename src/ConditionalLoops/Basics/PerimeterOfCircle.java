//Perimeter Of Circle
package ConditionalLoops.Basics;

public class PerimeterOfCircle {
    public static void main(String[] args) {
        double radius = 5.0; // Radius of the circle
        double perimeter = 2 * Math.PI * radius; // Perimeter formula for circle
        System.out.println("Perimeter of the circle with radius " + radius + " is: " + perimeter);
    }
}
