//Perimeter Of Equilateral Triangle
package ConditionalLoops.Basics;

public class PerimeterOfEquilateralTriangle {
    public static void main(String[] args) {
        double side = 5.0; // Length of the side of the equilateral triangle
        double perimeter = 3 * side; // Perimeter formula for equilateral triangle
        System.out.println("Perimeter of the equilateral triangle with side " + side + " is: " + perimeter);
    }
}
